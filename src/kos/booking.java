    private void kamar(){
        try {
            query = "SELECT kode_kamar FROM kamar";
            rs = st.executeQuery(query);
            while(rs.next()){
                room.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            System.out.println("Error"+e);
        }
    }
    
    
    private void getKamar(){
        try {
            query = "SELECT harga_sewa,lantai FROM kamar WHERE kode_kamar='"+room.getSelectedItem().toString()+"'";
            rs = st.executeQuery(query);
            if(rs.next()){
                harga.setText(rs.getString("harga_sewa"));
                lantai.setText(rs.getString("lantai"));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    private boolean isKamarFull(){
        try {
            query = "SELECT status_kamar FROM kamar WHERE kode_kamar='"+room.getSelectedItem().toString()+"'";
            st = con.createStatement();
            rs = st.executeQuery(query);
            if (rs.next()){
                if(rs.getString("status_kamar").equals("1")){
                    return true;
                }
                return false;
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return false;
    }
    
    private String totalTagihan(){
        int total;
        total = tagihann.get(0)*tagihann.get(1);
        String total1=String.valueOf(total);
        return total1;
    }
    
    private void getTagihan(){
        try {
            query = "SELECT * FROM penyewa NATURAL JOIN kamar WHERE id_penyewa='"+Login.id.get(0)+"'";
            st = con.createStatement();
            rs = st.executeQuery(query);
            if(rs.next()){
                nama_penyewa.setText(rs.getString("nama"));
                no_kamar.setText(rs.getString("kode_kamar"));
                no_handphone.setText(rs.getString("no_hp"));
                lama_sewa.setText(rs.getString("lama_sewa")+" Bulan");
                harga_sewa.setText("Rp "+rs.getString("harga_sewa")+"/Bulan");
                tagihann.add(0, rs.getInt("lama_sewa"));
                tagihann.add(1, rs.getInt("harga_sewa"));
                tagihan.setText("Rp "+totalTagihan());
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
