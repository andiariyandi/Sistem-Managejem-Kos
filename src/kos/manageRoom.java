    private void load_table(){
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Kode Kamar");
        model.addColumn("Harga Sewa");
        model.addColumn("Lantai");
        model.addColumn("Status");

        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String statuss;
            query = "SELECT * FROM kamar";
            st = con.createStatement();
            rs = st.executeQuery(query);
            while(rs.next()){
                if(rs.getString("status_kamar").equals("1")){
                    statuss = "Terisi";
                }else{
                    statuss = "Kosong";
                }
                model.addRow(new Object[]{no++,rs.getString("kode_kamar"),rs.getString("harga_sewa"),rs.getString("lantai")
                ,statuss});
            }
            jTable1.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        }
    
    
