    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            query = "INSERT INTO kamar(kode_kamar,harga_sewa,lantai,status_kamar) VALUES (?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, kodeKamar.getText());
            pst.setString(2, hargaSewa.getText());
            pst.setString(3, lantai.getSelection().getActionCommand());
            pst.setString(4, status.getSelection().getActionCommand());
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "Kamar Berhasil Ditambah!");
            load_table();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
                
    }//GEN-LAST:event_jButton3ActionPerformed

