    private void insertTagihan(){
        try {
            query = "INSERT INTO tagihan(kode_tagihan,id_penyewa,total_tagihan) VALUES (?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, id_tagihan.getText());
            pst.setString(2, Login.id.get(0));
            pst.setString(3, totalTagihan());
            pst.execute();
            pst.close();
        } catch (SQLException ex) {
            Logger.getLogger(booking.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
