    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String kode_kamar = jTable1.getValueAt(baris, 1).toString();
        kodeKamar.setText(kode_kamar);
        String lantaii =jTable1.getValueAt(baris, 3).toString();
        if(lantaii.equals("1")){
            l1.setSelected(true);
        }else{
            l2.setSelected(true);
        }
        String harga_sewa =jTable1.getValueAt(baris, 2).toString();
        hargaSewa.setText(harga_sewa);
        String statuss =jTable1.getValueAt(baris, 4).toString();
        if(statuss.equals("Kosong")){
            kosong.setSelected(true);
        }else{
            isi.setSelected(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

